// If_Else Statement 
// Print If_True Condition Statement 

public class If_Else01 {

	public static void main(String[] args) {
		// Initialization
		int g = 66;
		int y = 45;
		int z = g+y; // Addition
		if (z<g) {
			System.out.println("z is greater than g"); // True Statement Print On Console
		}
		else {
			System.out.println("g is not greater than z"); // If False T
		}

	}

}

// Switch Case Type
public class SwitchStrings {

    public static void main(String[] args) {
    	
        String course = "Python";
        
        switch (course) {
        
            case "Python":
                System.out.println("Python was made by Guido Van Rossum");
                break;
                
            case "Java":
                System.out.println("Java was made by James Gosling in 1995 & developed by the Sun-Micro System.");
                break;
                
            case "C++":
                System.out.println("C ++ was made by Bjarne Stroustrup");
                break;
           }
       }
   }
// If_Else Statement 
// Print Else_True Condition Statement 
public class If_Else02 {

	public static void main(String[] args) {
		
		int r = 26;
		int e = 12;
		int t = r*e;
		
		if (t<r) {
			System.out.println("t is less than r"); // False Statement 
		}
		else {
			System.out.println("t is greater than r"); // True Statement Is Print On Console
		}

	}

}
